package com.sf.it.hackathon.breakingbot.model;

public class BreakingBotDTO {
	
	private final int id;
	private final String name;
	public BreakingBotDTO(int id, String name){
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	
	

}
